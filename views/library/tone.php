<div class="row mb-5">
    <div class="col-md-12">
        <h4>Voice</h4>
        <p>Our design principles apply to the words used throughout our products to ensure that language seamlessly
            integrates with visual design. We apply these principles to the product voice with language-specific
            guidelines.</p>

        <div class="row">
            <div class="col-md-4 p-4">
                <h6>Rational</h6>
                <strong>Clear and understandable</strong>
                <ul class="link-list-menu" style="text-align:justify">
                    <li> * Ensure decisions about grammar and mechanics are research-informed and tested.</li>
                    <li> * Avoid language that's overly opinionated, funny, or trendy.</li>
                </ul>
            </div>
            <div class="col-md-4 p-4">
                <h6>Human</h6>
                <strong>Friendly, honest, and responsible</strong>
                <ul class="link-list-menu" style="text-align:justify">
                    <li> * Vary sentence style and structure for readability and relatability.</li>
                    <li> * Acknowledge and account for users' emotions.</li>
                </ul>
            </div>
            <div class="col-md-4 p-4">
                <h6>Focused</h6>
                <strong>Concise and simple</strong>
                <ul class="link-list-menu" style="text-align:justify">
                    <li> * Describe only what's needed, without unnecessary decoration.</li>
                    <li> * Avoid creating new concepts and only name things when necessary.</li>
                </ul>
            </div>
        </div>
    </div>
</div>

<div class="row mb-5">
    <div class="col-md-12">
        <h4>Tone</h4>
        <p>
            If voice is our products' personality, then tone is all the different ways we express that personality.
            <br />
            Tone can vary depending on the people we talk to, as well as the situations in which we talk to those
            people. For example, though you have one personality, you might still speak to a family member differently
            than you would a neighbor. You might also speak to your family member in different ways if they were sad,
            happy, or frustrated.
            <br />
            The same thing applies to how we talk to users.
            Tone is a powerful tool that can both shape a positive user experience for people, as well as clear the air
            in a negative one.
        </p>
    </div>

    <div class="col-md-12 mt-3">
        <h6>Tone Guide</h6>
        <p>
            The tone spectrum acts as a sliding scale for considering which of our attitudes and expressions are
            appropriate for different experiences.
            <br>The right tone depends on a user's contextual needs and corresponding emotions for that experience, as
            well as how involved and emotional we need to be in the process.
        </p>

